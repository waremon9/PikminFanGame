package Application;

/**
 *
 * @author verhi
 */
public class Zone {
    private int id;
    private String name;
    private String image;

    public Zone(int id, String name, String image) {
        this.id = id;
        this.name = name;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }
}
